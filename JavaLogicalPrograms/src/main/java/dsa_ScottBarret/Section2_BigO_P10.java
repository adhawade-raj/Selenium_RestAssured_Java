package dsa_ScottBarret;

public class Section2_BigO_P10 {

	/**
	 * O(1)
	 * @param n
	 */
	
	/**O(1)*/
	public static int printStream(int n) {
		return n+n;
	}
	
	/**O(1)*/
	public static int printStream2(int n) {
		return n+n+n;
	}
	
	public static void main(String[] args) {
		
		printStream(5);
	}


}

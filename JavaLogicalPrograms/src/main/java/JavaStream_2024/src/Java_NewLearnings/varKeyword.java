package Java_NewLearnings;

public class varKeyword {

	public static void main(String[] args) {

		var x = 10;
		System.out.println(x);

		var y = "Raj";
		System.out.println(y);

		var z = 'R';
		System.out.println(z);

		var a = true;
		System.out.println(a);

		var b = 10.00;
		System.out.println(b);

	}

}
